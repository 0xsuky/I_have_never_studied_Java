public class AutoBoxingUnboxing {
  public static void main(String[] args) {
    Integer iValue = 10;
    Double dValue = 3.14;

    System.out.println(iValue);
    System.out.println(dValue);

    int num1 = iValue;
    double num2 = dValue;
    System.out.println(num1);
    System.out.println(num2);
  }
}
